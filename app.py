@app.route("/api/log", methods=["GET"])
@app.route("/log", methods=["GET"])
@limiter.limit("30 per minute")
def log():
    """
    Return the most recent audit log entries as JSON.
    Query params:
      limit (int, default 50) — max entries to return
    """
    limit = min(int(request.args.get("limit", 50)), 200)
    rows  = get_recent_submissions(limit)

    entries = [
        {
            "content_id":  r["content_id"],
            "creator_id":  r["author_id"],
            "timestamp":   r["created_at"],
            "attribution": r["classification"],
            "confidence":  r["confidence_score"],
            "llm_score":   r["llm_score"],
            "status":      r["status"],
        }
        for r in rows
    ]

    return jsonify({"entries": entries, "total": len(entries)}), 200


